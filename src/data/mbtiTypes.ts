export const getMBTIGroup = (code: string) => {
  if (["INTJ", "INTP", "ENTJ", "ENTP"].includes(code)) return "분석가형";
  if (["INFJ", "INFP", "ENFJ", "ENFP"].includes(code)) return "외교관형";
  if (["ISTJ", "ISFJ", "ESTJ", "ESFJ"].includes(code)) return "관리자형";
  return "탐험가형";
};

export const getGroupColor = (code: string) => {
  const group = getMBTIGroup(code);
  switch (group) {
    case "분석가형": return "bg-purple-100 text-purple-700";
    case "외교관형": return "bg-green-100 text-green-700";
    case "관리자형": return "bg-blue-100 text-blue-700";
    case "탐험가형": return "bg-yellow-100 text-yellow-700";
    default: return "bg-gray-100 text-gray-700";
  }
};

